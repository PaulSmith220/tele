                <a href="#navigator"><img height="20"  src="/public/img/up.png" alt="К содержанию"/></a><a name="Режим правки">Режим правки</a><p><br>
                    Когда добавление необходимых пунктов закончено, возможно ручное редактирование отчета.
                    <br> Данный пункт не является обязательным<br>
                    Для перехода в режим правки, нажмите кнопку <b>Ручная правка</b><br>
                    Редактор примет такой вид:<br>
                     <center><img  src="/public/likbez/12.png"/></center><br>   
                     При этом пропадет верхнее меню, а текст в редакторе станет доступен для редактирования.
                     Функционал редактора схож с функционалом таких программ, как WordPad, Microsoft Word и т.п.
                     <br>Позволяется форматирование и стилизация текста.
                     <br>
                     Чтобы выйти из режима правки и добавить или убрать ещё пункты меню, нажмите кнопку <b>Отмена</b>.
                     Редактор вернется в исходное состояние.
                     <br>
                     Внимание, при любых изменениях, все произведенные вами вручную изменения будут утерянны!<bR>
                     Используйте режим ручной правки, когда добавление стандартных пунктов уже закончено.<br>
                     <hr>
                 <a href="#navigator"><img height="20"  src="/public/img/up.png" alt="К содержанию"/></a><a name="Когда все готово">Когда все готово</a><p><br>    
                     Карта полностью заполнена, нужные файлы приложены и вы закончили редактирование?<br>
                     Осталось выбрать специалиста - выберите нужного человека в выпадающем списке блока управления слева.
                     Отлично. Кнопка <img  src="/public/likbez/13.png" alt="Отправить"/> завершит создание вашей консультации.<br>
                     Консультация будет направлена специалисту, а вы увидите результат редактирования в открывшейся странице.<br>
                     Увидеть данную консультацию далее вы сможете на главной странице в разделе <b>Отправленные</b>.<br>
                     Если ваша консультация выделена жирным - значит, адресат ещё не прочел её.
                 <hr>
                     <a href="#navigator"><img height="20"  src="/public/img/up.png" alt="К содержанию"/></a><a name="Перенаправление консультации">Перенаправление консультации</a><p><br>  
                         Для того чтобы перенаправить консультацию другому специалисту, нажмите кнопку
                         <img  src="/public/likbez/14.png" alt="Перенаправить"/><br>
                         Откроется выпадающее меню:<BR>
                      <center><img  src="/public/likbez/15.png"/></center><br>   
                      Введите текст комментария при перенаправлении - его увидит специалист, которому вы перенаправите консультацию в графе <b>Комментарий при перенаправлении</b><br>
                      Выберите специалиста из списка и щелкните на его фамилии - консультация будет перенаправлена данному специалисту.
